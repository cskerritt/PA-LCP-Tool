# 8. Methodology & standards
# --------------------------------------------------------------------------- #
def _methodology(wb: Workbook) -> None:
    ws = wb.create_sheet("Methodology & Standards")
    S.set_widths(ws, [4, 110])
    row = S.title_block(
        ws, "Methodology & Professional Standards",
        "Basis for admissibility under Daubert / FRE 702", ncols=2)
    for heading, paragraphs in METHODOLOGY_SECTIONS:
        hc = ws.cell(row=row, column=1, value="")
        hc2 = ws.cell(row=row, column=2, value=heading)
        hc2.font = S.CATEGORY_FONT
        row += 1
        for para in paragraphs:
            bullet = ws.cell(row=row, column=1, value="•")
            bullet.font = S.BODY_FONT
            bullet.alignment = S.LEFT
            cc = ws.cell(row=row, column=2, value=para)
            cc.font = S.BODY_FONT
            cc.alignment = S.LEFT_WRAP
            # Estimate wrapped height.
            est_lines = max(1, int(len(para) / 95) + 1)
            ws.row_dimensions[row].height = 15 * est_lines
            row += 1
        row += 1
    S.page_setup(ws, landscape=False)


