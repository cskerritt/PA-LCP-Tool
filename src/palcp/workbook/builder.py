# 9. Medical foundation
# --------------------------------------------------------------------------- #
def _medical_foundation(wb: Workbook, result: ProjectionResult) -> None:
    ws = wb.create_sheet("Medical Foundation")
    S.set_widths(ws, [24, 32, 50, 14])
    row = S.title_block(
        ws, "Medical Foundation Index",
        "Each item's basis in a treating-provider recommendation or record "
        "(Gunn; Anderson-Moody)", ncols=4)
    S.header_row(ws, row, ["Category", "Item", "Medical foundation", "Status"])
    header_at = row
    row += 1
    missing = 0
    for r in result.item_results:
        it = r.item
        ws.cell(row=row, column=1, value=it.category).font = S.BODY_FONT
        ws.cell(row=row, column=2, value=it.item).font = S.BODY_FONT
        fc = ws.cell(row=row, column=3, value=it.medical_foundation or "")
        fc.font = S.BODY_FONT
        fc.alignment = S.LEFT_WRAP
        if it.medical_foundation:
            status, color = "documented", "006100"
        else:
            status, color = "MISSING", "9C0006"
            missing += 1
        sc = ws.cell(row=row, column=4, value=status)
        sc.font = S.status_font(color)
        sc.alignment = S.CENTER
        row += 1
    row += 1
    S.note(ws, row,
           f"{missing} item(s) lack a documented medical foundation. A "
           f"non-physician planner may not supply medical opinions; items "
           f"without a treating-provider basis are vulnerable to exclusion.",
           ncols=4)
    ws.freeze_panes = ws.cell(row=header_at + 1, column=1)
    S.page_setup(ws, landscape=False, repeat_header=header_at)


# --------------------------------------------------------------------------- #
# 10. Validation
# --------------------------------------------------------------------------- #
def _validation(wb: Workbook, validation: ValidationReport) -> None:
    ws = wb.create_sheet("Validation")
    S.set_widths(ws, [12, 26, 28, 70])
    row = S.title_block(
        ws, "Daubert / FRE 702 Pre-Flight Validation",
        validation.summary(), ncols=4)
    S.header_row(ws, row, ["Severity", "Peer-review domain", "Item", "Finding"])
    header_at = row
    row += 1
    if not validation.findings:
        S.note(ws, row, "No findings. All checks passed.", ncols=4)
    for f in validation.sorted():
        color = {"ERROR": "9C0006", "WARN": "9C6500", "INFO": "385723"}[f.severity]
        sc = ws.cell(row=row, column=1, value=f.severity)
        sc.font = S.status_font(color)
        sc.alignment = S.CENTER
        ws.cell(row=row, column=2, value=f.domain).font = S.BODY_FONT
        ws.cell(row=row, column=3, value=f.item or "—").font = S.BODY_FONT
        mc = ws.cell(row=row, column=4, value=f.message)
        mc.font = S.BODY_FONT
        mc.alignment = S.LEFT_WRAP
        ws.row_dimensions[row].height = 28
        row += 1
    ws.freeze_panes = ws.cell(row=header_at + 1, column=1)
    S.page_setup(ws, landscape=True, repeat_header=header_at)


# --------------------------------------------------------------------------- #
# Orchestration
# --------------------------------------------------------------------------- #
def build_workbook(
    result: ProjectionResult,
    validation: ValidationReport,
    *,
    version: str = "0.1.0",
    generated_on: str = "",
) -> Workbook:
    """Assemble and return the full :class:`~openpyxl.Workbook`."""
    wb = Workbook()
    _cover(wb, result, version, generated_on)
    _summary(wb, result)
    _current_cost(wb, result)
    _pv_projection(wb, result)
    _annual_schedule(wb, result)
    _assumptions(wb, result)
    _data_sources(wb, result)
    _methodology(wb)
    _medical_foundation(wb, result)
    _validation(wb, validation)
    wb.properties.title = "Life Care Plan Cost Projection"
    wb.properties.creator = result.plan.evaluator or "PA-LCP-Tool"
    wb.properties.subject = result.plan.matter
    return wb


def save_workbook(
    result: ProjectionResult,
    validation: ValidationReport,
    path: str,
    *,
    version: str = "0.1.0",
    generated_on: str = "",
) -> str:
    wb = build_workbook(result, validation, version=version, generated_on=generated_on)
    parent = Path(path).parent
    if str(parent) not in ("", "."):
        parent.mkdir(parents=True, exist_ok=True)
    wb.save(path)
    return path
