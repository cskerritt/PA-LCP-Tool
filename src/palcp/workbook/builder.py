"""Build the multi-tab Excel workbook from a projection result.

Tabs (in order):

1. **Cover** -- caption, claimant, evaluator, dates, headline totals, disclaimer.
2. **Summary** -- totals by category in all three measures + key assumptions.
3. **Current-Cost Tables** -- itemised current (base-year) dollars by category.
4. **PV Projection** -- itemised lifetime nominal & present value by category.
5. **Annual Schedule** -- year-by-year nominal / discount factor / PV.
6. **Assumptions & Rates** -- every rate, its basis, source, and the formulas.
7. **Data Sources** -- pricing-source provenance + standard source descriptions.
8. **Methodology & Standards** -- IALCP standards, consensus statements, Daubert.
9. **Medical Foundation** -- per-item foundation citations (flags gaps).
10. **Validation** -- the Daubert pre-flight findings.

Each tab is self-explanatory on paper so an exhibit can be lifted out and stand
on its own.
"""

from __future__ import annotations

from pathlib import Path
from typing import Optional

