# 6. Assumptions & rates
# --------------------------------------------------------------------------- #
def _assumptions(wb: Workbook, result: ProjectionResult) -> None:
    plan = result.plan
    ws = wb.create_sheet("Assumptions & Rates")
    S.set_widths(ws, [34, 16, 14, 40, 18])
    row = S.title_block(
        ws, "Assumptions & Rate Sources",
        "Every rate and figure relied upon, with its published source", ncols=5)

    ws.cell(row=row, column=1, value="VALUATION").font = S.CATEGORY_FONT
    row += 1
    row = _kv(ws, row, "Report date", plan.report_date)
    row = _kv(ws, row, "Base year", plan.base_year)
    le = plan.life_expectancy
    row = _kv(ws, row, "Age at report", le.age_at_report)
    row = _kv(ws, row, "Life expectancy (additional yrs)",
              round(le.additional_years, 2))
    row = _kv(ws, row, "  └ source", le.source or "(none cited)")
    row = _kv(ws, row, "  └ as of / note", f"{le.as_of} {le.note}".strip())
    row += 1

    dr = plan.discount_rate
    ws.cell(row=row, column=1, value="DISCOUNT RATE").font = S.CATEGORY_FONT
    row += 1
    row = _kv(ws, row, "Annual discount rate", dr.annual_rate, value_fmt=S.FMT_PCT2)
    row = _kv(ws, row, "Basis", dr.basis)
    row = _kv(ws, row, "Cash-flow timing", dr.timing)
    row = _kv(ws, row, "Source", dr.source or "(none cited)")
    row = _kv(ws, row, "As of", dr.as_of)
    row += 1

    ws.cell(row=row, column=1, value="PRICE-GROWTH SERIES").font = S.CATEGORY_FONT
    row += 1
    S.header_row(ws, row, ["Key", "Annual rate", "Net disc.", "Source", "As of"])
    row += 1
    for key, gr in plan.growth_rates.items():
        # Only show series actually referenced by an item, plus 'general'.
        used = any(it.growth_key == key for it in plan.items) or key == "general"
        if not used:
            continue
        ws.cell(row=row, column=1, value=f"{key} — {gr.label}").font = S.BODY_FONT
        ws.cell(row=row, column=2, value=gr.annual_rate).number_format = S.FMT_PCT2
        from ..economics.projection import net_discount_rate
        ws.cell(row=row, column=3,
                value=net_discount_rate(dr.annual_rate, gr.annual_rate)
                ).number_format = S.FMT_PCT2
        sc = ws.cell(row=row, column=4, value=gr.source or "(none cited)")
        sc.font = S.BODY_FONT
        sc.alignment = S.LEFT_WRAP
        ws.cell(row=row, column=5, value=gr.as_of).font = S.BODY_FONT
        row += 1
    row += 1

    ws.cell(row=row, column=1, value="METHOD & FORMULAS").font = S.CATEGORY_FONT
    row += 1
    timing = dr.timing
    offset = {"begin_year": "y", "mid_year": "y + 0.5", "end_year": "y + 1"}[timing]
    formulas = [
        f"Cash-flow time from valuation for projection year y (0-based): m(y) = {offset}  [timing = {timing}].",
        "Future (nominal) cost:  nominal_i(y) = current_i(y) × (1 + g_i)^m(y).",
        "Present value:          pv_i(y) = nominal_i(y) / (1 + d)^m(y).",
        "Equivalent net-discount form:  pv_i(y) = current_i(y) / (1 + r_i)^m(y),  r_i = (1+d)/(1+g_i) − 1.",
        "Reasonable-value percentile policy: {:g}th percentile applied consistently across sources.".format(plan.percentile_policy),
        "Final partial year of life expectancy is prorated by its fractional portion.",
    ]
    for line in formulas:
        row = S.note(ws, row, line, ncols=5)
    if plan.collateral_source_note:
        row += 1
        ws.cell(row=row, column=1, value="COLLATERAL SOURCE NOTE").font = S.CATEGORY_FONT
        row += 1
        row = S.note(ws, row, plan.collateral_source_note, ncols=5)
    S.page_setup(ws, landscape=False)


