# --------------------------------------------------------------------------- #
def _annual_schedule(wb: Workbook, result: ProjectionResult) -> None:
    ws = wb.create_sheet("Annual Schedule")
    cats = result.categories
    base_headers = ["Yr", "Calendar yr", "Age"]
    tail_headers = ["Total nominal", "Discount factor", "Total PV", "Cumulative PV"]
    headers = base_headers + [f"{c} (nom)" for c in cats] + tail_headers
    widths = [6, 12, 8] + [16] * len(cats) + [18, 14, 18, 20]
    S.set_widths(ws, widths)
    row = S.title_block(
        ws, "Annual Cost Schedule",
        "Future (nominal) cost by category, with discounting to present value",
        ncols=len(headers))
    S.header_row(ws, row, headers)
    header_at = row
    row += 1

    cum_pv = 0.0
    for yr in result.year_results:
        cum_pv += yr.total_pv
        ws.cell(row=row, column=1, value=yr.year_index + 1).alignment = S.CENTER
        ws.cell(row=row, column=2,
                value=(yr.calendar_year if yr.calendar_year else "")
                ).alignment = S.CENTER
        ws.cell(row=row, column=3, value=round(yr.age_start, 0)).alignment = S.CENTER
        col = 4
        for c in cats:
            S.money(ws.cell(row=row, column=col,
                            value=round(yr.by_category_nominal.get(c, 0.0), 2)))
            col += 1
        S.money(ws.cell(row=row, column=col, value=round(yr.total_nominal, 2)))
        ws.cell(row=row, column=col + 1,
                value=round(yr.discount_factor, 4)).number_format = "0.0000"
        ws.cell(row=row, column=col + 1).alignment = S.RIGHT
        S.money(ws.cell(row=row, column=col + 2, value=round(yr.total_pv, 2)))
        S.money(ws.cell(row=row, column=col + 3, value=round(cum_pv, 2)))
        if (row % 2) == 0:
            for cc in range(1, len(headers) + 1):
                ws.cell(row=row, column=cc).fill = S.BAND_FILL
        row += 1

    # Total row (nominal + PV).
    tc = ws.cell(row=row, column=3, value="TOTAL")
    tc.font = S.BODY_BOLD
    tc.alignment = S.RIGHT
    col = 4
    for c in cats:
        v = sum(yr.by_category_nominal.get(c, 0.0) for yr in result.year_results)
        cc = ws.cell(row=row, column=col, value=round(v, 2))
        S.money(cc)
        cc.font = S.BODY_BOLD
        cc.border = S.BORDER_TOP
        col += 1
    for offset, val in ((0, result.grand_total_nominal),
                        (2, result.grand_total_present_value)):
        cc = ws.cell(row=row, column=col + offset, value=round(val, 2))
        S.money(cc)
        cc.font = S.BODY_BOLD
        cc.border = S.BORDER_TOP_DOUBLE
    ws.freeze_panes = ws.cell(row=header_at + 1, column=4)
    S.page_setup(ws, landscape=True, repeat_header=header_at)


