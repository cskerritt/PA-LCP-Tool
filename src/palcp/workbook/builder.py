# 7. Data sources
# --------------------------------------------------------------------------- #
def _data_sources(wb: Workbook, result: ProjectionResult) -> None:
    plan = result.plan
    ws = wb.create_sheet("Data Sources")
    S.set_widths(ws, [30, 10, 14, 24, 18, 30])
    row = S.title_block(
        ws, "Data Sources & Provenance",
        "Pricing sources actually used in this plan, with retrieval detail",
        ncols=6)

    S.header_row(ws, row, ["Pricing source", "%ile", "Geographic basis",
                           "Used for (examples)", "Retrieval/effective", "Citation"])
    header_at = row
    row += 1

    # Aggregate per source.
    agg: dict[str, dict] = {}
    for it in plan.items:
        src = it.pricing_source or "(unspecified)"
        a = agg.setdefault(src, {"pcts": set(), "geos": set(), "items": [],
                                 "dates": set(), "cites": set()})
        if it.percentile is not None:
            a["pcts"].add(it.percentile)
        if it.geographic_basis:
            a["geos"].add(it.geographic_basis)
        if len(a["items"]) < 3:
            a["items"].append(it.item)
        if it.retrieval_date:
            a["dates"].add(it.retrieval_date)

    for src, a in agg.items():
        ws.cell(row=row, column=1, value=src).font = S.BODY_FONT
        ws.cell(row=row, column=2,
                value=", ".join(f"{p:g}" for p in sorted(a["pcts"])) or "—"
                ).alignment = S.CENTER
        ws.cell(row=row, column=3, value=", ".join(sorted(a["geos"])) or "—"
                ).alignment = S.LEFT_WRAP
        ws.cell(row=row, column=4, value="; ".join(a["items"])).alignment = S.LEFT_WRAP
        ws.cell(row=row, column=5, value=", ".join(sorted(a["dates"])) or "—"
                ).alignment = S.LEFT_WRAP
        cite = SOURCE_DESCRIPTIONS.get(src, {}).get("url", "")
        ws.cell(row=row, column=6, value=cite).alignment = S.LEFT_WRAP
        row += 1

    row += 1
    ws.cell(row=row, column=1,
            value="STANDARD SOURCE DESCRIPTIONS (reference)").font = S.CATEGORY_FONT
    row += 1
    for name, meta in SOURCE_DESCRIPTIONS.items():
        nc = ws.cell(row=row, column=1, value=name)
        nc.font = S.BODY_BOLD
        nc.alignment = S.LEFT_WRAP
        dc = ws.cell(row=row, column=2, value=meta["desc"])
        ws.merge_cells(start_row=row, start_column=2, end_row=row, end_column=6)
        dc.font = S.BODY_FONT
        dc.alignment = S.LEFT_WRAP
        ws.row_dimensions[row].height = 28
        row += 1
    S.page_setup(ws, landscape=True, repeat_header=header_at)


