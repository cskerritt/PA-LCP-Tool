from .content import METHODOLOGY_SECTIONS, SOURCE_DESCRIPTIONS


def _kv(ws: Worksheet, row: int, label: str, value, *, value_is_money=False,
        value_fmt: Optional[str] = None) -> int:
    """Write a 'Label | value' pair (label in col A, value in col B)."""
    lc = ws.cell(row=row, column=1, value=label)
    lc.font = S.LABEL_FONT
    lc.alignment = S.LEFT
    vc = ws.cell(row=row, column=2, value=value)
    vc.font = S.BODY_FONT
    vc.alignment = S.LEFT_WRAP
    if value_is_money:
        S.money(vc)
        vc.alignment = S.RIGHT
    elif value_fmt:
        vc.number_format = value_fmt
    return row + 1


# --------------------------------------------------------------------------- #
# 1. Cover
# --------------------------------------------------------------------------- #
def _cover(wb: Workbook, result: ProjectionResult, version: str, generated_on: str) -> None:
    plan = result.plan
    ws = wb.active
    ws.title = "Cover"
    S.set_widths(ws, [34, 60])
    row = S.title_block(
        ws,
        "Life Care Plan — Cost Projection",
        plan.matter or "Future Medical Care Cost Analysis",
        ncols=2,
    )

    row += 0
    ws.cell(row=row, column=1, value="CASE & EVALUATOR").font = S.CATEGORY_FONT
    row += 1
    row = _kv(ws, row, "Matter / Caption", plan.matter)
    row = _kv(ws, row, "Jurisdiction", plan.jurisdiction)
    row = _kv(ws, row, "Evaluator", plan.evaluator)
    row = _kv(ws, row, "Credentials", plan.evaluator_credentials)
    row = _kv(ws, row, "Report date", plan.report_date)
    row += 1

    ws.cell(row=row, column=1, value="CLAIMANT").font = S.CATEGORY_FONT
    row += 1
    cl = plan.claimant
    row = _kv(ws, row, "Name", cl.name)
    row = _kv(ws, row, "Date of birth", cl.dob)
    row = _kv(ws, row, "Sex", cl.sex)
    row = _kv(ws, row, "Age at report", cl.age_at_report)
    row = _kv(ws, row, "Residence / geographic basis", cl.residence)
    row = _kv(ws, row, "Life expectancy (additional years)",
              round(plan.life_expectancy.additional_years, 1))
    row = _kv(ws, row, "Projection horizon to age",
              round(plan.life_expectancy.terminal_age, 1))
    row += 1

    ws.cell(row=row, column=1, value="HEADLINE TOTALS").font = S.CATEGORY_FONT
    row += 1
    row = _kv(ws, row, "Total current annual cost (Year 1, base $)",
              round(result.first_year_current_cost, 2), value_is_money=True)
    row = _kv(ws, row, "Lifetime cost — current (undiscounted) $",
              round(result.grand_total_current, 2), value_is_money=True)
    row = _kv(ws, row, "Lifetime cost — future (nominal, grown) $",
              round(result.grand_total_nominal, 2), value_is_money=True)
    row = _kv(ws, row, "Lifetime cost — PRESENT VALUE",
              round(result.grand_total_present_value, 2), value_is_money=True)
    ws.cell(row=row - 1, column=1).font = S.BODY_BOLD
    ws.cell(row=row - 1, column=2).font = S.BODY_BOLD
    row += 1

    disclaimer = (
        "This workbook was generated by PA-LCP-Tool v{v} on {d}. It presents cost "
        "projections based on the inputs and published rate/source citations "
        "documented on the Assumptions & Rates and Data Sources tabs. Costs are "
        "stated in current (base-year) dollars, grown to the year incurred using "
        "the cited medical price series, and discounted to present value at the "
        "cited rate using the stated timing convention. Medical care opinions and "
        "their foundation are addressed on the Medical Foundation tab and remain "
        "the responsibility of the qualified clinician(s) of record."
    ).format(v=version, d=generated_on)
    row = S.note(ws, row, disclaimer, ncols=2)
    ws.row_dimensions[row - 1].height = 90
    S.page_setup(ws, landscape=False)


# --------------------------------------------------------------------------- #
# 2. Summary
# --------------------------------------------------------------------------- #
def _summary(wb: Workbook, result: ProjectionResult) -> None:
    plan = result.plan
    ws = wb.create_sheet("Summary")
    S.set_widths(ws, [40, 22, 22, 22])
    row = S.title_block(
        ws, "Summary of Future Care Costs",
        "By category — current, nominal, and present value", ncols=4)

    S.header_row(ws, row, ["Category", "Current (undiscounted)",
                           "Future (nominal)", "Present value"])
    row += 1
    totals = result.totals_by_category()
    for cat in result.categories:
        t = totals.get(cat, {"current": 0, "nominal": 0, "present_value": 0})
        ws.cell(row=row, column=1, value=cat).font = S.BODY_FONT
        for col, key in ((2, "current"), (3, "nominal"), (4, "present_value")):
            c = ws.cell(row=row, column=col, value=round(t[key], 2))
            S.money(c)
        if (row % 2) == 0:
            for col in range(1, 5):
                ws.cell(row=row, column=col).fill = S.BAND_FILL
        row += 1

    # Grand total row.
    c = ws.cell(row=row, column=1, value="TOTAL")
    c.font = S.BODY_BOLD
    c.fill = S.TOTAL_FILL
    for col, val in (
        (2, result.grand_total_current),
        (3, result.grand_total_nominal),
        (4, result.grand_total_present_value),
    ):
        cc = ws.cell(row=row, column=col, value=round(val, 2))
        S.money(cc)
        cc.font = S.BODY_BOLD
        cc.fill = S.TOTAL_FILL
        cc.border = S.BORDER_TOP_DOUBLE
    ws.cell(row=row, column=1).border = S.BORDER_TOP_DOUBLE
    row += 2

    ws.cell(row=row, column=1, value="KEY ASSUMPTIONS").font = S.CATEGORY_FONT
    row += 1
    le = plan.life_expectancy
    dr = plan.discount_rate
    row = _kv(ws, row, "Life expectancy (yrs)", round(le.additional_years, 1))
    row = _kv(ws, row, "Discount rate", dr.annual_rate, value_fmt=S.FMT_PCT2)
    row = _kv(ws, row, "Discount basis / timing", f"{dr.basis} / {dr.timing}")
    row = _kv(ws, row, "Reasonable-value percentile policy",
              f"{plan.percentile_policy:g}th")
    S.note(ws, row + 1,
           "Detailed rate sources and formulas appear on the Assumptions & Rates "
           "tab. Per-item detail appears on the Current-Cost Tables and PV "
           "Projection tabs.", ncols=4)
    S.page_setup(ws, landscape=False)


