# 3 & 4. Itemised tables (current-cost and PV)
# --------------------------------------------------------------------------- #
def _current_cost(wb: Workbook, result: ProjectionResult) -> None:
    ws = wb.create_sheet("Current-Cost Tables")
    widths = [22, 30, 12, 18, 9, 14, 12, 10, 10, 10, 18]
    S.set_widths(ws, widths)
    row = S.title_block(
        ws, "Current-Cost Life Care Plan Tables",
        "Annualized costs in current (base-year) dollars — no growth, no discount",
        ncols=len(widths))
    headers = ["Category", "Item", "Code", "Source", "%ile", "Unit cost",
               "Freq/Yr", "Every N yr", "1st age", "Last age",
               "Lifetime (undiscounted)"]
    S.header_row(ws, row, headers)
    header_at = row
    row += 1

    by_cat: dict[str, list] = {}
    for r in result.item_results:
        by_cat.setdefault(r.item.category, []).append(r)

    for cat in result.categories:
        rows_for_cat = by_cat.get(cat, [])
        if not rows_for_cat:
            continue
        # Category banner row.
        ws.cell(row=row, column=1, value=cat).font = S.CATEGORY_FONT
        for col in range(1, len(headers) + 1):
            ws.cell(row=row, column=col).fill = S.CATEGORY_FILL
        row += 1
        cat_total = 0.0
        for r in rows_for_cat:
            it = r.item
            ws.cell(row=row, column=1, value=it.category).font = S.BODY_FONT
            ws.cell(row=row, column=2, value=it.item).font = S.BODY_FONT
            ws.cell(row=row, column=3, value=it.code or "").font = S.BODY_FONT
            ws.cell(row=row, column=4, value=it.pricing_source or "").font = S.BODY_FONT
            pcell = ws.cell(row=row, column=5,
                            value=(it.percentile if it.percentile is not None else ""))
            pcell.font = S.BODY_FONT
            pcell.alignment = S.CENTER
            S.money(ws.cell(row=row, column=6, value=round(it.unit_cost, 2)), cents=True)
            ws.cell(row=row, column=7,
                    value=(it.frequency_per_year if it.frequency_per_year else "")
                    ).alignment = S.CENTER
            ws.cell(row=row, column=8,
                    value=(it.every_n_years if it.every_n_years else "")
                    ).alignment = S.CENTER
            ws.cell(row=row, column=9,
                    value=(round(r.first_age, 0) if r.first_age is not None else "")
                    ).alignment = S.CENTER
            ws.cell(row=row, column=10,
                    value=(round(r.last_age, 0) if r.last_age is not None else "")
                    ).alignment = S.CENTER
            S.money(ws.cell(row=row, column=11, value=round(r.current_total, 2)))
            cat_total += r.current_total
            row += 1
        # Category subtotal.
        sc = ws.cell(row=row, column=10, value=f"{cat} subtotal")
        sc.font = S.BODY_BOLD
        sc.alignment = S.RIGHT
        tc = ws.cell(row=row, column=11, value=round(cat_total, 2))
        S.money(tc)
        tc.font = S.BODY_BOLD
        tc.border = S.BORDER_TOP
        row += 1

    # Grand total.
    gc = ws.cell(row=row, column=10, value="LIFETIME TOTAL (undiscounted)")
    gc.font = S.BODY_BOLD
    gc.alignment = S.RIGHT
    gt = ws.cell(row=row, column=11, value=round(result.grand_total_current, 2))
    S.money(gt)
    gt.font = S.BODY_BOLD
    gt.border = S.BORDER_TOP_DOUBLE
    ws.freeze_panes = ws.cell(row=header_at + 1, column=1)
    S.page_setup(ws, landscape=True, repeat_header=header_at)


def _pv_projection(wb: Workbook, result: ProjectionResult) -> None:
    ws = wb.create_sheet("PV Projection")
    widths = [22, 30, 14, 12, 12, 12, 20, 20, 20]
    S.set_widths(ws, widths)
    row = S.title_block(
        ws, "Present-Value Cost Projection",
        "Lifetime cost grown to year incurred and discounted to present value",
        ncols=len(widths))
    headers = ["Category", "Item", "Growth series", "Growth %",
               "Discount %", "Net disc %", "Lifetime current $",
               "Lifetime nominal $", "Present value"]
    S.header_row(ws, row, headers)
    header_at = row
    row += 1

    by_cat: dict[str, list] = {}
    for r in result.item_results:
        by_cat.setdefault(r.item.category, []).append(r)

    d = result.plan.discount_rate.annual_rate
    for cat in result.categories:
        rows_for_cat = by_cat.get(cat, [])
        if not rows_for_cat:
            continue
        ws.cell(row=row, column=1, value=cat).font = S.CATEGORY_FONT
        for col in range(1, len(headers) + 1):
            ws.cell(row=row, column=col).fill = S.CATEGORY_FILL
        row += 1
        sub_cur = sub_nom = sub_pv = 0.0
        for r in rows_for_cat:
            it = r.item
            gr = result.plan.growth_rate_for(it)
            ws.cell(row=row, column=1, value=it.category).font = S.BODY_FONT
            ws.cell(row=row, column=2, value=it.item).font = S.BODY_FONT
            ws.cell(row=row, column=3, value=gr.label).font = S.BODY_FONT
            ws.cell(row=row, column=4, value=r.growth_rate).number_format = S.FMT_PCT2
            ws.cell(row=row, column=5, value=d).number_format = S.FMT_PCT2
            ws.cell(row=row, column=6,
                    value=r.net_discount_rate).number_format = S.FMT_PCT2
            S.money(ws.cell(row=row, column=7, value=round(r.current_total, 2)))
            S.money(ws.cell(row=row, column=8, value=round(r.nominal_total, 2)))
            S.money(ws.cell(row=row, column=9, value=round(r.present_value, 2)))
            sub_cur += r.current_total
            sub_nom += r.nominal_total
            sub_pv += r.present_value
            row += 1
        lc = ws.cell(row=row, column=6, value=f"{cat} subtotal")
        lc.font = S.BODY_BOLD
        lc.alignment = S.RIGHT
        for col, val in ((7, sub_cur), (8, sub_nom), (9, sub_pv)):
            cc = ws.cell(row=row, column=col, value=round(val, 2))
            S.money(cc)
            cc.font = S.BODY_BOLD
            cc.border = S.BORDER_TOP
        row += 1

    gc = ws.cell(row=row, column=6, value="LIFETIME TOTAL")
    gc.font = S.BODY_BOLD
    gc.alignment = S.RIGHT
    for col, val in (
        (7, result.grand_total_current),
        (8, result.grand_total_nominal),
        (9, result.grand_total_present_value),
    ):
        cc = ws.cell(row=row, column=col, value=round(val, 2))
        S.money(cc)
        cc.font = S.BODY_BOLD
        cc.border = S.BORDER_TOP_DOUBLE
    ws.freeze_panes = ws.cell(row=header_at + 1, column=1)
    S.page_setup(ws, landscape=True, repeat_header=header_at)


